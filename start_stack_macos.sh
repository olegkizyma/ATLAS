# –§—É–Ω–∫—Ü—ñ—è –ø–µ—Ä–µ–≤—ñ—Ä–∫–∏ —Å–µ—Ä–≤—ñ—Å—É
check_service() {
    local name="$1"
    local url="$2"
    local timeout="${3:-5}"
    
    log_debug "Checking $name at $url..."
    
    if curl -s --max-time "$timeout" "$url" > /dev/null 2>&1; then
        log_info "‚úÖ $name - AVAILABLE"
        return 0
    else
        log_warn "‚ö†Ô∏è  $name - NOT AVAILABLE"
        return 1
    fi
}

# –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ macOS —Å–ø–µ—Ü–∏—Ñ—ñ—á–Ω–∏—Ö –≤–∏–º–æ–≥
check_macos_requirements() {
    log_info "üçé Checking macOS requirements..."
    
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ Homebrew (—Ä–µ–∫–æ–º–µ–Ω–¥–æ–≤–∞–Ω–æ)
    if command -v brew &> /dev/null; then
        log_info "‚úÖ Homebrew - AVAILABLE"
    else
        log_warn "‚ö†Ô∏è  Homebrew not found - install recommended: /bin/bash -c \"\$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)\""
    fi
    
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ Python 3.8+
    if command -v python3 &> /dev/null; then
        local python_version=$(python3 --version | cut -d' ' -f2)
        log_info "‚úÖ Python ${python_version} - OK"
    else
        log_error "‚ùå Python 3 required but not found"
        log_error "   Install: brew install python3"
        exit 1
    fi
    
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ curl
    if command -v curl &> /dev/null; then
        log_info "‚úÖ curl - OK"
    else
        log_error "‚ùå curl required but not found"
        exit 1
    fi
    
    log_info "‚úÖ macOS requirements check completed"
}

