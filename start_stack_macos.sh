prepare_legacy_environment() {
    log_info "üîß Preparing legacy (frontend_new) environment..."
    if [ ! -d "frontend_new" ]; then
        log_error "‚ùå frontend_new directory not found"
        exit 1
    fi
    cd frontend_new
    if [ ! -d "venv" ]; then
        log_info "üêç Creating Python virtual environment (legacy)..."
        python3 -m venv venv
    fi
    source venv/bin/activate
    log_info "üì¶ Installing/updating Python deps (legacy)..."
    pip install -r requirements.txt --quiet --upgrade
    cd ..
}

start_legacy_system() {
    log_intelligent "üß† Starting ATLAS Legacy Stack (frontend_new + orchestrator) ..."
    local ROOT_DIR="$(pwd)"
    local LOG_DIR="$ROOT_DIR/logs"
    mkdir -p "$LOG_DIR"

    # Start orchestrator (Node.js)
    if pgrep -f "frontend_new/orchestrator/server.js" > /dev/null; then
        log_warn "‚ö†Ô∏è  Orchestrator already running"
    else
        (cd frontend_new/orchestrator && \
            if [ ! -d node_modules ]; then npm install --quiet; fi && \
            LOG_DIR="$LOG_DIR" nohup node server.js > "$LOG_DIR/orchestrator.log" 2>&1 & echo $! > "$LOG_DIR/orchestrator.pid")
        if [ -f "$LOG_DIR/orchestrator.pid" ]; then
            log_info "üöÄ Orchestrator started (PID: $(cat "$LOG_DIR/orchestrator.pid"))"
        else
            log_warn "‚ö†Ô∏è  Orchestrator PID file not found"
        fi
    fi

    # Start Flask frontend
    if pgrep -f "frontend_new/app/atlas_server.py" > /dev/null; then
        log_warn "‚ö†Ô∏è  Legacy Flask already running"
    else
        (cd frontend_new && source venv/bin/activate && \
            LOG_DIR="$LOG_DIR" nohup python app/atlas_server.py > "$LOG_DIR/frontend.log" 2>&1 & echo $! > "$LOG_DIR/frontend.pid")
        if [ -f "$LOG_DIR/frontend.pid" ]; then
            log_info "üöÄ Legacy frontend started (PID: $(cat "$LOG_DIR/frontend.pid"))"
        else
            log_warn "‚ö†Ô∏è  Legacy frontend PID file not found"
        fi
    fi

    # Wait for health
    log_info "‚è≥ Waiting for legacy health (10s)..."
    for i in $(seq 1 10); do
        if curl -s http://127.0.0.1:5001/api/health > /dev/null 2>&1; then
            log_intelligent "‚úÖ Legacy Web Interface is responding"
            break
        fi
        sleep 1
    done
}

show_legacy_status() {
